//Method chaining using inheritance

package Learning_1;

public class ParentMethod 
{

	public void ParentMethod1()
	{
		System.out.println("Parent Default method");
	}
	public void ParentMethod1(int a)
	{
		System.out.println("Parent 1 parameter method");
	}
	public void ParentMethod1(int a, int b)
	{
		System.out.println("Parent 2 parmeter method");
	}
	public void ParentMethod1(int a, int b, int c)
	{
		System.out.println("Parent 3 parameter method");
	}
	public void ParentMethod1(int a, int b, int c, int d)
	{
		System.out.println("Parent 4 parameter method");
	}
	
}
